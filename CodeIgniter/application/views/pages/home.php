<br/>Ferner ist der <b>Essbereich</b> mehr denn je in den Mittelpunkt ger&uuml;ckt. Hand aufs Herz- bei den
meisten Menschen findet das Leben heute am gro&szlig;en Esstisch in der <b>K&uuml;che</b> statt. Kochen ist
wieder in, auch zusammen mit den G&auml;sten. Da liegt es doch nahe, den gro&szlig;en Esstisch im <b>Koch-Wohnbereich</b>
in Szene zu setzen. Gro&szlig;e Tische, bei denen wir voll und ganz auf die W&uuml;nsche unserer
<b>Kunden</b> eingehen, geh&ouml;ren ebenso zu <b>unseren Leistungen</b> wie die Sitzbank in gerader Form
oder auch als Eckbank, damit noch ein paar G&auml;ste mehr Platz finden. Und auch die <b>K&uuml;che</b> vom
Tischler ist ein durchaus interessantes Thema. Ein Tischler fertigt die einzelnen Schrankmodule genau so,
wie sie gebraucht werden, fern ab von Standardma&szlig;en. Individueller geht es kaum!
