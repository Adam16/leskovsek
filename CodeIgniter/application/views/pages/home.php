<b>Schiebet&uuml;rschr&auml;nke</b> sind derzeit der Renner und nicht nur superschick, sondern auch enorm
Platz sparend.&Uuml;brigens: Wir sind Profis in Sachen Schiebt&uuml;ren, auch in Verbindung mit unserer
neuen <b>Riefa-Leichtbauplatte</b>. Dieses &ouml;kologische Produkt verf&uuml;gt &uuml;ber beste w&auml;rme-und
schalld&auml;mmende Eigenschaften und ist dabei superleicht. Mehr unter der Rubrik: Riefa-Leichtbauplatte.
